package entities;

public class Calculadora {
	private double resuldado;

	public void somar(double num1, double num2){
		resuldado = num1+num2;
	}

	public double getResultado() {
		return getResultado();
	}

	public double getResuldado() {
		return resuldado;
	}

	public void setResuldado(double resuldado) {
		this.resuldado = resuldado;
	}
}
